    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:

        if (tbFornecedor.getSelectedRow() >= 0) {

            FornecedorModel modelo = (FornecedorModel) tbFornecedor.getValueAt(tbFornecedor.getSelectedRow(), 0);
            id = modelo.getId();
            txtContacto.setText(modelo.getContacto());
            txtContribuente.setText(modelo.getNumContribuente());
            txtEmail.setText(modelo.getEmail());
            txtLocalizacao.setText(modelo.getLocalizacao());
            txtNome.setText(modelo.getNome());
            tabulacao.setSelectedIndex(0);
        } else {
            JOptionPane.showMessageDialog(this, "Selecione o item");
        }
    }//GEN-LAST:event_btnEditarActionPerformed
private void limpar(){
    
    txtContacto.setText("");
    txtContribuente.setText("");
    txtEmail.setText("");
    txtLocalizacao.setText("");
    txtNome.setText("");
   id = 0;
    
}
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        if (tbFornecedor.getSelectedRow() >= 0) {

            FornecedorModel modelo = (FornecedorModel) tbFornecedor.getValueAt(tbFornecedor.getSelectedRow(), 0);
            int op = JOptionPane.showConfirmDialog(this, "Deseja eliminar o item", "Aviso", 0);
            if (op == JOptionPane.YES_OPTION) {

                FornecedorController controller = new FornecedorController();
                modelo.setEstado(new EstadoModel(3, ""));
                if (controller.update(modelo)) {
                    JOptionPane.showMessageDialog(this, "Operação realizada com sucesso");
                    id = 0;
                    carregar();
                    limpar();

                } else {
                    JOptionPane.showMessageDialog(this, "Ocorreu um erro ao realizar esta operação", "ERRO", JOptionPane.ERROR_MESSAGE);
                }

            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione o item");
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

