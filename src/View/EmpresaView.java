    public void addLogotipo() {
        String correspon = "";
        
        String logotipo = "";
        
        try {
            
            JFileChooser chooser = new JFileChooser();
            FileNameExtensionFilter filter = new FileNameExtensionFilter(
                    "PDF & PNG Images", "pdf", "png", "jpg", "gif", "tif");
            chooser.setFileFilter(filter);
            int returnVal = chooser.showOpenDialog(this);
            String pathFinal;
            
            if (returnVal == JFileChooser.APPROVE_OPTION) {
                File f = chooser.getSelectedFile();
                pathFinal = f.getAbsolutePath();
                
                correspon = f.getName();
                
                String aux = f.getName();
                logotipo = aux;
                model.setLogotipo(logotipo);
                //aux = "BannerFarmaciaSaudePopular.jpg";
                
                CopyFileUtil.copiarArquivos(f, new File("Relatorio"+File.separator + aux));
                ImageIcon imagem = new ImageIcon(pathFinal);
                lblLogotipo.setIcon(new ImageIcon(imagem.getImage().getScaledInstance(lblLogotipo.getWidth(), lblLogotipo.getHeight(), Image.SCALE_DEFAULT)));
                
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            
        }
        
    }
    
    public void destinoImg() {
        try {
            fcOrigem = origem.getChannel();
            fcDestino = destino.getChannel();
            fcOrigem.transferTo(0, fcOrigem.size(), fcDestino);//copiando o arquivo e salvando no diretório que você escolheu
            lblLogotipo.setText(model.getLogotipo());
//                JOptionPane.showMessageDialog(null,"Imagem Actualizado com Sucesso");
            origem.close();
            destino.close();
            
        } catch (Exception ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(null, "Formato de Ficheiro não Permitido", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }

