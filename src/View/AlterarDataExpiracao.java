    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:
        
        EntradaStockItemController itemController = new EntradaStockItemController();
        EntradaStockItemModel itemModelo = new EntradaStockItemModel();
        itemModelo.setCodBarra(cboCodigoBarra.getSelectedItem().toString());
        itemModelo.setDataExpiracao(DataComponent.getData(dataExpiracao));
        itemModelo.setEstado(new EstadoModel(1, ""));
        if(itemController.updateDataExpiracao(itemModelo)){
            JOptionPane.showMessageDialog(this, "Data de Expiração alterado com sucesso");
        }else{
            JOptionPane.showMessageDialog(this, "Data de Expiração não foi alterado ","ERRO",JOptionPane.ERROR_MESSAGE);
        }
                
    }//GEN-LAST:event_btnGravarActionPerformed

