    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:

        FornecedorModel modelo = new FornecedorModel();
        modelo.setId(id);
        modelo.setNome(txtNome.getText());
        modelo.setContacto(txtContacto.getText());
        modelo.setEmail(txtEmail.getText());
        modelo.setEstado(new EstadoModel(1, ""));
        modelo.setLocalizacao(txtLocalizacao.getText());
        modelo.setTipoFornecedor((TipoFornecedorModel) cboTipoFornecedor.getSelectedItem());
        modelo.setUsuario(usuario);
        modelo.setNumContribuente(txtContribuente.getText());
        modelo.setData(DataComponent.getDataActual());

        if (!modelo.isEmpty()) {

            FornecedorController controller = new FornecedorController();
            FornecedorModel modeloAux = controller.getAll(txtContribuente.getText());

            if (id == 0 && modeloAux.getId() > 0 && modeloAux.getEstado().getId() == 1) {
                JOptionPane.showMessageDialog(this, "Esta Fornecedor ja existe no sistema", "Alerta", JOptionPane.WARNING_MESSAGE);
                return;
            } else if (modeloAux.getId() > 0 && modeloAux.getEstado().getId() > 1) {
                modelo.setId(modeloAux.getId());
            }

            //-----------------------------------------------------------------------------
            System.out.println(modelo.getNumContribuente());
            if (controller.saveOrUpdate(modelo)) {
                JOptionPane.showMessageDialog(this, "Operação realizada com sucesso");
                LogUtil.log.salvarLog(TipoLog.INFO, " Salvou Fornecedor ( "+modelo.getNome()+" )");
                limpar();

            } else {
                 JOptionPane.showMessageDialog(this, "Ocorreu um erro ao realizar esta operacao\nCausa:\n\t1-Campo mal preenchido;\n\t2-quantidade carater excedido;", "Aviso", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Preencha o espaço em branco");
        }
    }//GEN-LAST:event_btnGravarActionPerformed
    private void carregar() {
        DefaultTableModel tb = (DefaultTableModel) tbFornecedor.getModel();
        tb.setRowCount(0);
        FornecedorController controller = new FornecedorController();
        List<FornecedorModel> lista = controller.get();

        for (FornecedorModel m : lista) {

            tb.addRow(new Object[]{
                m,
                m.getNumContribuente(),
                m.getContacto(),
                m.getEmail()
            });
        }
    }
