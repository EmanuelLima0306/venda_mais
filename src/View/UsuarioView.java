    private void eliminar() {

        int row = tbUsuario.getSelectedRow();

        if (row >= 0) {

            int op = JOptionPane.showConfirmDialog(this, "Deseja Eliminar este usuario", "Alerta", 0);

            if (op == JOptionPane.YES_OPTION) {

                UsuarioModel modelo = (UsuarioModel) tbUsuario.getValueAt(row, 0);
                UsuarioController controller = new UsuarioController();

                modelo.setEstado(new EstadoModel(3, ""));

                if (controller.updateEstado(modelo)) {

                    carregarUsuario();
                    JOptionPane.showMessageDialog(this, "Operacao Realizada com sucesso");
                } else {
                    JOptionPane.showMessageDialog(this, "Operacao invalida", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            }

        } else {
            JOptionPane.showMessageDialog(this, "Selecione o usuario");
        }
    }

    private void desactivar() {

        int row = tbUsuario.getSelectedRow();

        if (row >= 0) {

            UsuarioModel modelo = (UsuarioModel) tbUsuario.getValueAt(row, 0);
            UsuarioController controller = new UsuarioController();

            modelo.setEstado(new EstadoModel(2, ""));

            if (controller.updateEstado(modelo)) {

                carregarUsuario();
                JOptionPane.showMessageDialog(this, "Operacao Realizada com sucesso");
            } else {
                JOptionPane.showMessageDialog(this, "Operacao invalida", "ERRO", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Selecione o usuario");
        }
    }
    private void activar() {

        int row = tbUsuario.getSelectedRow();

        if (row >= 0) {

            UsuarioModel modelo = (UsuarioModel) tbUsuario.getValueAt(row, 0);
            UsuarioController controller = new UsuarioController();

            modelo.setEstado(new EstadoModel(1, ""));

            if (controller.updateEstado(modelo)) {

                carregarUsuario();
                JOptionPane.showMessageDialog(this, "Operacao Realizada com sucesso");
            } else {
                JOptionPane.showMessageDialog(this, "Operacao invalida", "ERRO", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Selecione o usuario");
        }
    }

    private void limpar() {

        txtConfrimar.setText("");
        txtContacto.setText("");
        txtEMail.setText("");
        txtNome.setText("");
        txtSenha.setText("");
        txtUsuario.setText("");
        txtTaxa.setText("0");
    }

    private void editar() {

        int row = tbUsuario.getSelectedRow();

        if (row >= 0) {

            UsuarioModel modelo = (UsuarioModel) tbUsuario.getValueAt(row, 0);

            txtContacto.setText(modelo.getContacto());
            txtEMail.setText(modelo.getEmail());
            txtNome.setText(modelo.getNome());
            txtUsuario.setText(modelo.getUsuario());

            TipoUsuarioModel tipoUsuario = modelo.getTipoUsuario();
            JComboBoxComponent.selectItem(cboTipoUsuario, tipoUsuario);

            id = modelo.getId();
            tabulacao.setSelectedIndex(0);

        } else {
            JOptionPane.showMessageDialog(this, "Selecione o usuario");
        }

    }

    private void carregarUsuario() {

        DefaultTableModel tb = (DefaultTableModel) tbUsuario.getModel();
        UsuarioController controller = new UsuarioController();
        List<UsuarioModel> lista = controller.get();
        tb.setRowCount(0);
        for (UsuarioModel modelo : lista) {
            tb.addRow(new Object[]{
                modelo,
                modelo.getUsuario(),
                modelo.getContacto()

            });
        }

    }
    private void carregarUsuarioFlitro() {

        DefaultTableModel tb = (DefaultTableModel) tbUsuario.getModel();
        UsuarioController controller = new UsuarioController();
        List<UsuarioModel> lista = controller.get(txtPesquisar.getText());
        tb.setRowCount(0);
        for (UsuarioModel modelo : lista) {
            tb.addRow(new Object[]{
                modelo,
                modelo.getUsuario(),
                modelo.getContacto()

            });
        }

    }
    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:

        UsuarioModel modelo = new UsuarioModel();
        modelo.setContacto(txtContacto.getText());
        modelo.setEmail(txtEMail.getText());
        modelo.setEstado(new EstadoModel(1, ""));
        modelo.setNome(txtNome.getText());
        modelo.setSenha(String.valueOf(txtSenha.getPassword()));
        modelo.setTipoUsuario((TipoUsuarioModel) cboTipoUsuario.getSelectedItem());
        modelo.setUsuario(txtUsuario.getText());
        modelo.setIdUsuario(usuarioLogado.getId());
        modelo.setId(id);
        modelo.setData(DataComponent.getDataActual());
        modelo.setTaxa(Calculo.getValueNormal(txtTaxa.getText()));

        if (!modelo.isUsuarioEmpty()) {

            if (isEqualsSenha_confirm()) {

                UsuarioController controller = new UsuarioController();
                if (controller.saveOrUpdate(modelo)) {
                    
                    LogUtil.log.salvarLog(TipoLog.INFO, " Gravou Usuário ( "+modelo.getNome()+" )");
                    limpar();
                    JOptionPane.showMessageDialog(this, "Operacao Realizada com sucesso");
                } else {
                    JOptionPane.showMessageDialog(this, "Operacao invalida", "ERRO", JOptionPane.ERROR_MESSAGE);
                }

            } else {
                JOptionPane.showMessageDialog(this, "As senhas não são compativel", "Aviso", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Preencha o espaço em branco");
        }
    }//GEN-LAST:event_btnGravarActionPerformed

