    private void produto() {
        
        ProdutoController controller = new ProdutoController();
        cboProduto.setModel(new DefaultComboBoxModel(controller.getProdutos("").toArray()));
    }
    
    private void lote() {
        
     
        ProdutoModel produto = (ProdutoModel) cboProduto.getSelectedItem();
        EntradaStockItemController controller = new EntradaStockItemController();
        cboCodigoBarra.setModel(new DefaultComboBoxModel(controller.getLoteCodigoBarra(produto.getId()).toArray()));
    }
    private void loteData() {
        
     
        ProdutoModel produto = (ProdutoModel) cboProduto.getSelectedItem();
        EntradaStockItemController controller = new EntradaStockItemController();
        String data = controller.getLoteDataExpiracao(produto.getId(),cboCodigoBarra.getSelectedItem().toString()).getDataExpiracao();
        
        dataExpiracao.setDate(DataComponent.stringParaData(data));
    }

