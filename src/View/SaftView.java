    public String getDataFacturaInicio() {
        GregorianCalendar gc = new GregorianCalendar();
        gc.setTime(dataInicio.getDate());
        String dataSelecionada = gc.get(GregorianCalendar.YEAR) + "-"
                + ((gc.get(GregorianCalendar.MONTH) + 1) < 10 ? "0" + (gc.get(GregorianCalendar.MONTH) + 1) : (gc.get(GregorianCalendar.MONTH) + 1)) + "-"
                + (gc.get(GregorianCalendar.DATE) < 10 ? "0" + gc.get(GregorianCalendar.DATE) : gc.get(GregorianCalendar.DATE));

        return dataSelecionada;
    }

    public String getDataFacturaFim() {
        GregorianCalendar gc = new GregorianCalendar();
        gc.setTime(dataFim.getDate());
        String dataSelecionada = gc.get(GregorianCalendar.YEAR) + "-"
                + ((gc.get(GregorianCalendar.MONTH) + 1) < 10 ? "0" + (gc.get(GregorianCalendar.MONTH) + 1) : (gc.get(GregorianCalendar.MONTH) + 1)) + "-"
                + (gc.get(GregorianCalendar.DATE) < 10 ? "0" + gc.get(GregorianCalendar.DATE) : gc.get(GregorianCalendar.DATE));

        return dataSelecionada;
    }

    public void gerarXML(File file) {
        Definicoes.file = file;

        OperacoesIva operacoesIva = new OperacoesIva();
        operacoesIva.setDataInicio(getDataFacturaInicio());
        operacoesIva.setDataFinal(getDataFacturaFim());

        operacoesIva.dadosOperacoes();
    }

