    private void reporQtd( FacturaModel modelo){
        
        FacturaItemController controller = new FacturaItemController();
        List<FacturaItemModel> lista = controller.getItemByIdFactura(modelo.getId());
        for(FacturaItemModel f : lista){
            
            AumentarQtd(f.getLote(), f.getQtd());
        }
    }
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (cboFactura.getSelectedIndex() >= 0) {

            if (!txtMotivo.getText().isEmpty()) {

                FacturaModel modelo = (FacturaModel) cboFactura.getSelectedItem();
                modelo.setObs(txtMotivo.getText());

                FacturaController fController = new FacturaController();
                if (fController.eliminarFactura(modelo)) {

                    carregarFactura();
                    reporQtd(modelo);
                    JOptionPane.showMessageDialog(this, "Factura Eliminada com sucesso");
                } else {
                    JOptionPane.showMessageDialog(this, "Factura nao foi eliminada", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Preencha o campo de motivo");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Selecione o a factura");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed
    private void carregarFactura() {

        FacturaController fController = new FacturaController();
        String dataIni = DataComponent.getData(dataInicio);
        String dataF = DataComponent.getData(dataFim);
        cboFactura.setModel(new DefaultComboBoxModel(fController.getFacturas(dataIni, dataF).toArray()));
    }

