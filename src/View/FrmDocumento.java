    public void Salvar() {

        DocumentoController12 controller = new DocumentoController12();
        Documento modelo = new Documento(id, txtDoc.getText().trim(), txtNext.getText().trim());
        if (controller.saveOrUpdate(modelo)) {
            Limpar();
            carregarDocumento();
            JOptionPane.showMessageDialog(null, "Operacao realizada Com Sucesso!...");
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao Adicionar!...");
        }
    }

    public void Limpar() {
        txtDoc.setText("");
        txtNext.setText("");
        jLabelNextActual.setText("");
    }
