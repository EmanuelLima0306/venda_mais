    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:

        model.setContacto(txtContacto.getText());
        model.setEmail(txtEmail.getText());
        model.setEndereco(txtEndereco.getText());
        model.setInfoConta(txtInfo.getText());
        model.setNif(txtNIf.getText());
        model.setWebSIte(txtWebSite.getText());
        model.setNome(txtNome.getText());
        model.setRegime((TipoRegimeModel) cboTipoRegime.getSelectedItem());
        model.setId(id);
        model.setLocal(txtLocal.getText());
//        model.setLogotipo(lblLogotipo.getText());
        
        if (!model.isEmpty()) {
            
            EmpresaController controller = new EmpresaController();
            if (controller.saveOrUpdate(model)) {
                JOptionPane.showMessageDialog(this, "Operacao realizada com sucesso");
            } else {
                JOptionPane.showMessageDialog(this, "Operacao nao foi realizada com sucesso", "Alerta", JOptionPane.WARNING_MESSAGE);
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Preecnha o espaço em branco");
        }

    }//GEN-LAST:event_btnGravarActionPerformed

