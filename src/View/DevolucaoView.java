    private void ItemFactura() {

        int idFactura = Integer.parseInt(txtNumFactura.getText());
        FacturaModel factura = new FacturaModel();
        factura.setId(idFactura);
        for (int i = 0; i < tbItemFactura.getRowCount(); i++) {

            Boolean estado = (Boolean) tbItemFactura.getValueAt(i, 7);

            FacturaItemModel item = (FacturaItemModel) tbItemFactura.getValueAt(i, 0);
            FacturaModel f = new FacturaModel();
            f.setId(idFactura);
            item.setFactura(f);

            if (estado != null) {

                if (estado.booleanValue()) {

                    FacturaItemController controller = new FacturaItemController();

                    // ProdutoModel p = item.getProduto();
                    item.setEstado(new EstadoModel(5, ""));

                    controller.update(item);

                    AumentarQtd(item.getLote(), item.getQtd());

                } else {

                    factura.setTotalApagar(factura.getTotalApagar() + item.getSubTotal() + item.getIva() - item.getDesconto());
                    factura.setTotalDesconto(factura.getTotalDesconto() + item.getDesconto());

                }
            } else {

                factura.setTotalApagar(factura.getTotalApagar() + item.getSubTotal() + item.getIva() - item.getDesconto());
                factura.setTotalDesconto(factura.getTotalDesconto() + item.getDesconto());

            }
//122813.25
//117563.25
        }
        factura(factura);

    }

