private void limpar() {

        txtCodBarraActual.setText("");
        txtCodBarraNovo.setText("");
    }
    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:
        if (!txtCodBarraActual.getText().isEmpty() && !txtCodBarraNovo.getText().isEmpty()) {

            if (!txtCodBarraActual.getText().equalsIgnoreCase(txtCodBarraNovo.getText())) {

                EntradaStockItemController itemController = new EntradaStockItemController();
                if (itemController.getCodBarraExist(txtCodBarraActual.getText())) {

                    if (itemController.updateCodBarra(txtCodBarraActual.getText(), txtCodBarraNovo.getText())) {
                        limpar();
                        JOptionPane.showMessageDialog(this, "código de barra alterado com sucesso");
                    } else {
                        JOptionPane.showMessageDialog(this, "Operação invalida", "ERRO", JOptionPane.ERROR_MESSAGE);
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "O código de Barra Actual não existe no sistema");
                }

            } else {
                JOptionPane.showMessageDialog(this, "Os Código de barra não podem ser iguais");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Preencha o espço em branco");
        }
    }//GEN-LAST:event_btnGravarActionPerformed

