    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:

        ClienteModel modelo = new ClienteModel();
        modelo.setContacto(txtContacto.getText());
        modelo.setEmail(txtEmail.getText());
        modelo.setEndereco(txtEndereco.getText());
        modelo.setEstado(new EstadoModel(1, ""));
        modelo.setNome(txtNome.getText());
        modelo.setTipoCliente((TipoClienteModel) cboTipo.getSelectedItem());
        modelo.setId(id);
        modelo.setNif(txtNif.getText());
        modelo.setUsuario(usuario);
        modelo.setLimiteCredito(Double.parseDouble(txtLimiteCredito.getText()));
        //-------------------------------------------------------
        double valorAntigo = Double.parseDouble(txtValorCarteira.getText());
        double valorNovo = Double.parseDouble(txtValor.getText());
        //-------------------------------------------------------

        if (!modelo.isEmpty()) {
            
            if (valorNovo >= 0) {
                
                modelo.setValorCarteira(valorAntigo + valorNovo);
                ClienteController controller = new ClienteController();
                ClienteModel modeloAux = controller.getAll(txtContacto.getText());
                
                if(modeloAux != null)
                    if (id == 0 && modeloAux.getId() > 0 && modeloAux.getEstado().getId() == 1) {
                        JOptionPane.showMessageDialog(this, "Esta Fornecedor ja existe no sistema", "Alerta", JOptionPane.WARNING_MESSAGE);
                        return;
                    } else if (modeloAux.getId() > 0 && modeloAux.getEstado().getId() > 1) {
                        modelo.setId(modeloAux.getId());
                    }

                //-----------------------------------------------------------------------------
                if (controller.saveOrUpdate(modelo)) {
                    JOptionPane.showMessageDialog(this, "Opera√ß√£o realizada com sucesso");
                    LogUtil.log.salvarLog(TipoLog.INFO, " Salvou Cliente ( "+modelo.getNome()+" )");
                    limpar();
                    
                } else {
                    JOptionPane.showMessageDialog(this, "Ocorreu um erro ao realizar esta opera√ß√£o", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "O valor Adicionar na carteira n√£o pode ser negativo");
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Preencha o espa√ßo em branco");
        }
    }//GEN-LAST:event_btnGravarActionPerformed

