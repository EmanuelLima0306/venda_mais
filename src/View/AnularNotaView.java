    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:

        if (cboTipoDocumento.getSelectedIndex() > 0) {

            MovimentoController controller = new MovimentoController();
            String tipoDoc = cboTipoDocumento.getSelectedIndex() == 1 ? "C" : "D";
            List<Movimento> lista = controller.getByTipo(tipoDoc,
                    DataComponent.getData(dataOperacao));

            DefaultTableModel tb = (DefaultTableModel) tbMovimento.getModel();
            tb.setRowCount(0);
            for (Movimento m : lista) {

                tb.addRow(new Object[]{
                    m,
                    m.getValor(),
                    m.getFactura().getId(),
                    false

                });
            }

        } else {
            JOptionPane.showMessageDialog(this, "Selecione o tipo de documento");
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void tbMovimentoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMovimentoMouseClicked
        // TODO add your handling code here:

        DefaultTableModel tb = (DefaultTableModel) tbMovimento.getModel();
        int coluna = tbMovimento.getSelectedColumn();

        if (coluna == 3) {

            int linha = tbMovimento.getSelectedRow();
            Boolean isSelecionado = (boolean) tbMovimento.getValueAt(linha, coluna);

            if (isSelecionado) {

                Movimento model = (Movimento) tbMovimento.getValueAt(linha, 0);
                model.setEstado(new EstadoModel(2, ""));//DESACTIVADO

                FacturaModel factura = model.getFactura();
                factura.setEstado(new EstadoModel(1, ""));//ACTIVADO

                MovimentoController controller = new MovimentoController();
                if (controller.updateStatus(model)) {

                    FacturaController fController = new FacturaController();
                    if (fController.updateEstado(factura)) {
                        JOptionPane.showMessageDialog(this, "Operacao realizada com sucesso");
                        tb.setRowCount(0);
                    } else {
                        JOptionPane.showMessageDialog(this, "Operacao nao foi finalizada com exito");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Operacao nao foi finalizada com exito");
                }

            } else {

                JOptionPane.showMessageDialog(this, "Selecione a opcao");

            }
        }

    }//GEN-LAST:event_tbMovimentoMouseClicked

