    private void txtNomeCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtNomeCaretUpdate
        
        if (!txtNome.getText().trim().isEmpty()) {
            ClienteController controller = new ClienteController();
            List<ClienteModel> lista = controller.get(txtNome.getText());
            
            DefaultListModel model = new DefaultListModel();
            for (ClienteModel m : lista) {
                model.addElement(m);
            }
            ListaCliente.setModel(model);
        }
    }//GEN-LAST:event_txtNomeCaretUpdate
    private void carregarInfo(ClienteModel modelo, boolean op) {
        
        JComboBoxComponent.selectItem(cboTipo, modelo.getTipoCliente());
        txtContacto.setText(modelo.getContacto());
        txtEmail.setText(modelo.getEmail());
        txtEndereco.setText(modelo.getEndereco());
        txtNome.setText(modelo.getNome());
        txtNif.setText(modelo.getNif());
        id = modelo.getId();
        txtValorCarteira.setText(String.valueOf(modelo.getValorCarteira()));
        
        enableComponent(op);
    }

    private void enableComponent(boolean flag) {
        
        cboTipo.setEnabled(false);
        txtContacto.setEnabled(false);
        txtEmail.setEnabled(false);
        txtEndereco.setEnabled(false);
        txtNome.setEnabled(false);
        txtNif.setEnabled(flag);
        
    }
    private void habilitarComponent() {
        
        cboTipo.setEnabled(true);
        txtContacto.setEnabled(true);
        txtEmail.setEnabled(true);
        txtEndereco.setEnabled(true);
        txtNome.setEnabled(true);
        txtNif.setEnabled(true);
        
    }

    private boolean isFacturaEmitida(ClienteModel modelo) {
        
        FacturaController controller = new FacturaController();
        return controller.isFacturaEmitidaByCliente(modelo.getId());
        
    }
