    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
          if (id > 0) {

            int op = JOptionPane.showConfirmDialog(this, "Deseja realmente eliminar o Armazém", "Alerta", 0);
            if (op == JOptionPane.YES_OPTION) {

                ArmazemController controller = new ArmazemController();
                ArmazemModel modelo = (ArmazemModel) cboArmazem.getSelectedItem();
                modelo.setEstado(new EstadoModel(3, ""));
                if (controller.update(modelo)) {

                    JOptionPane.showMessageDialog(this, "Armazém eliminada com sucesso");
                    LogUtil.log.salvarLog(TipoLog.INFO, " ELiminou Armazem ( "+modelo.getDesignacao()+")");
                    limpar();
                    carregado();
                   

                } else {
                    JOptionPane.showMessageDialog(this, "Ocorreu um erro ao eliminar Armazém", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            }

        } else {
            JOptionPane.showMessageDialog(this, "Selecione o Armazém");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

