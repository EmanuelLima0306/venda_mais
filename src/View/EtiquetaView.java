    private void produto() {

        ProdutoController controller = new ProdutoController();
        cboProduto.setModel(new DefaultComboBoxModel(controller.getByDesignacao(txtPesquisar.getText()).toArray()));
    }

    private void lote() {

        ProdutoModel produto = (ProdutoModel) cboProduto.getSelectedItem();
        EntradaStockItemController controller = new EntradaStockItemController();
        cboLote.setModel(new DefaultComboBoxModel(controller.getByProdutoActivo(produto).toArray()));
    }

