    public void carregar() {
        Limpar();
        DocumentoController12 controller = new DocumentoController12();

        jLabelNextActual.setText(String.valueOf(controller.getLastInsertAno(cboDocumento.getSelectedItem().toString())));
        txtNext.setText(String.valueOf(controller.getLastInsertAno(cboDocumento.getSelectedItem().toString())));
        txtDoc.setText(cboDocumento.getSelectedItem().toString());

        Documento modelo = (Documento) cboDocumento.getSelectedItem();
        id = modelo.getId();
        btnGravar.setEnabled(true);
      
    }
