    private void carregar() {
        if (!txtNumFactura.getText().isEmpty()) {
            FacturaItemController controller = new FacturaItemController();

            List<FacturaItemModel> lista = controller.getItemByIdFactura(Integer.parseInt(txtNumFactura.getText()), cboCliente.getSelectedItem().toString());
            DefaultTableModel tb = (DefaultTableModel) tbItemFactura.getModel();
            tb.setRowCount(0);
            for (FacturaItemModel model : lista) {

                tb.addRow(new Object[]{
                    model,
                    model.getQtd(),
                    model.getPreco(),
                    model.getSubTotal(),
                    model.getIva(),
                    model.getDesconto(),
                    model.getTotal()
                });
            }
        }
    }
