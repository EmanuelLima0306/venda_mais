    private void factura(FacturaModel modelo) {

        FacturaController controller = new FacturaController();
        FacturaModel f = controller.getById(modelo.getId());

        double totalApagar = modelo.getTotalApagar();
        double troco = 0;
        if (f.getFormaPagamento().getId() != 4) {
             troco = totalApagar - f.getValorEntregue() + f.getValorMulticaixa();
        }
        f.setTroco(troco);
        f.setTotalApagar(totalApagar);
        f.setTotalDesconto(modelo.getTotalDesconto());

        controller.updateValor(f);

    }

