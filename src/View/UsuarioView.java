    public void verificarParamentro(){
        
        ParamentroController paramentroController = new ParamentroController();
            ParamentroModel paramentroModel = paramentroController.getById(3);
            
            if(paramentroModel.getValor() != 3){
                txtTaxa.setVisible(false);
                lbTaxa.setVisible(false);
                txtUsuario.setSize(cboTipoUsuario.getSize());
            }
    }
    
    public void verifiCarPermissaoDarPermissao(){
        MenuItemUsuarioController modeloController = new MenuItemUsuarioController();
        MenuItemUsuarioModel modelo = modeloController.getPermissaoDarPermissaoByUsuario(usuarioLogado.getId());
        btnPermissao.setEnabled(modelo.getIdEstado() == 1);
    }
